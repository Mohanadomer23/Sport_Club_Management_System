    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try{
            int row = jTable1.getSelectedRow();
            String strNO = jTable1.getValueAt(row,0).toString();
            String strName = jTable1.getValueAt(row,1).toString();
            String strDate = jTable1.getValueAt(row,2).toString();
            String strEmail = jTable1.getValueAt(row,3).toString();
            String strSub = jTable1.getValueAt(row,4).toString();
            
            IDField.setText(strNO);
            NameField.setText(strEmail);
            DateField.setText(strDate);
            EmailField.setText(strName);
            SubField.setText(strSub);
            
            Addbtn.setEnabled(false);
            Updatebtn.setEnabled(true);
            Deletebtn.setEnabled(true);
            
        }
        catch(Exception ex){
            Tools.msgBox(ex.getMessage());
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jTable1MouseClicked

