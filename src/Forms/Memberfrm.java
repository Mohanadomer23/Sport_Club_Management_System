    Member ch = new Member();
    private void clearData()
    {
        Tools.clearText(this);
        IDField.setText(ch.getAutoNumber());
        Addbtn.setEnabled(true);
        Updatebtn.setEnabled(false);
        Deletebtn.setEnabled(false);
        NameField.requestFocus();
        
        
    }
    
     private void setValues()
    {
        
            ch.setMemberId(Integer.parseInt(IDField.getText()));
            ch.setFirstName(NameField.getText());
            ch.setJoinDate(DateField.getText());
            ch.setEmail(EmailField.getText());
            ch.setSubscriptionId(Integer.parseInt(SubField.getText()));
           
            
    }
     
